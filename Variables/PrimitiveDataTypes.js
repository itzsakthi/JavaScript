// Primitive Data Types :
    // String - Letters, Alphabets or Anything surrounded by Single or Double Quotes
    // Number - whether Integer or Decimal
    // Boolean - true or false
    // Undefined - if not assigned a value
    // Null - intentional absence of value



// String :
let single = 'Hello';
let double = "Hello";

// Number : 
let amount = 6;
let price = 4.99;

// Boolean :
let lateToWork = true;
let isMarried = false;

// Undefined :
var a;
console.log(a);   // Prints: undefined

// Null :
let x = null;
console.log(x);

