// Access Modifiers :

    // The concepts of public, private, and protected methods are collectively referred to as access modifiers or visibility modifiers in object-oriented programming (OOP). These modifiers control the accessibility or visibility of class members (properties and methods) to manage encapsulation and restrict access from outside the class.



// Summary of Access Modifiers:

    // Public: Accessible from anywhere, inside or outside the class.
    // Private: Accessible only within the class where they are defined.
    // Protected: Accessible within the class and its subclasses (not natively supported in JavaScript, but can be simulated).